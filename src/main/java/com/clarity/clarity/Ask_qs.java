    private void qry_submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qry_submit_btnActionPerformed
String facultyName = cmbFaculty.getSelectedItem().toString();
    String queryText = st_qry_get.getText().trim();
    int studentId = Integer.parseInt(Datas.st_uname_sp_get); 
    String st_year = Datas.get_Year;

    if (queryText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your query.");
        return;
    }

    // Generate session ID
    String prefix = "Y1-" + LocalDate.now().format(DateTimeFormatter.BASIC_ISO_DATE); // e.g., Y1-20250906
    String sqlGetLast = "SELECT session_id FROM queries WHERE session_id LIKE ? ORDER BY session_id DESC LIMIT 1";

    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
         PreparedStatement pstLast = con.prepareStatement(sqlGetLast)) {

        pstLast.setString(1, prefix + "%");
        ResultSet rs = pstLast.executeQuery();

        int nextSeq = 1;
        if (rs.next()) {
            String lastId = rs.getString("session_id");
            String[] parts = lastId.split("-");
            nextSeq = Integer.parseInt(parts[2]) + 1;
        }

        String newSessionId = prefix + "-" + String.format("%03d", nextSeq);

        // Insert query
        String sqlInsert = "INSERT INTO queries (session_id, student_id, faculty_name, query_text, year) VALUES (?, ?, ?, ?,?)";
        try (PreparedStatement pstInsert = con.prepareStatement(sqlInsert)) {
            pstInsert.setString(1, newSessionId);
            pstInsert.setInt(2, studentId);
            pstInsert.setString(3, facultyName);
            pstInsert.setString(4, queryText);
            pstInsert.setString(5, st_year);
            
            

            int rows = pstInsert.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Query submitted successfully!");
                st_qry_get.setText(""); // clear textbox
            } else {
                JOptionPane.showMessageDialog(this, "Failed to submit query.");
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }

    }//GEN-LAST:event_qry_submit_btnActionPerformed

