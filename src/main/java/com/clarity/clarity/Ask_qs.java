private void loadFacultyNames() {
    String sql = "SELECT name FROM faculty_details ORDER BY name ASC";
    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        cmbFaculty.removeAllItems();
        cmbFaculty.addItem("Select Faculty"); // ✅ Add this ONCE before the loop

        while (rs.next()) {
            cmbFaculty.addItem(rs.getString("name")); // ✅ Add actual names only
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading faculty names: " + e.getMessage());
    }
}




