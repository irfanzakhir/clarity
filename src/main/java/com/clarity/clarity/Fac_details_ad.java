    private void Fac_fetchDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Fac_fetchDetailsActionPerformed
        // TODO add your handling code here:
        try {
            // Load JDBC driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Connect to database
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clarity", "root", "");

            // SQL query
            String query = "SELECT * FROM faculty_details WHERE status = 'active'";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            // Get table model
            DefaultTableModel model = (DefaultTableModel) FacDetails_table.getModel();
            model.setRowCount(0); // Clear existing rows

            // Populate table
            while (rs.next()) {
                int id = rs.getInt("No");
                String uname = rs.getString("Name");
                int pword = rs.getInt("Password");
                

                model.addRow(new Object[]{id, uname, pword});
            }

            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading data: " + e.getMessage());
        }
    }//GEN-LAST:event_Fac_fetchDetailsActionPerformed

