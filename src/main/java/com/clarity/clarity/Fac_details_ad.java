    private void Fac_addDetails1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Fac_addDetails1ActionPerformed
        // TODO add your handling code here:
    int row = FacDetails_table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a faculty to delete.");
        return;
    }

    String facultyName = FacDetails_table.getValueAt(row, 0).toString(); // assuming column 0 is faculty_name

    int confirm = JOptionPane.showConfirmDialog(this,
        "Are you sure you want to delete this faculty?",
        "Confirm Delete", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        String sql = "UPDATE faculty_details SET status = 'deleted' WHERE faculty_name = ?";

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/clarity", "root", "");
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setString(1, facultyName);
            int rows = pst.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Faculty marked as deleted.");
                // Optionally refresh table
                //fetchFaculty(); // your method to reload active faculty
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting faculty: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_Fac_addDetails1ActionPerformed

