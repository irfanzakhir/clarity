    private void Upt_facActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Upt_facActionPerformed
        // TODO add your handling code here:
   int row = FacDetails_table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a faculty to update.");
        return;
    }

    String facultyName = FacDetails_table.getValueAt(row, 1).toString(); // assuming column 1 is Name

    // 🔹 Prompt for new password
    JPasswordField pwdField = new JPasswordField();
    int option = JOptionPane.showConfirmDialog(this, pwdField, 
        "Enter New Password for " + facultyName, JOptionPane.OK_CANCEL_OPTION);

    if (option == JOptionPane.OK_OPTION) {
        String newPassword = new String(pwdField.getPassword()).trim();

        if (newPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password cannot be empty.");
            return;
        }

        String sql = "UPDATE faculty_details SET Password = ? WHERE Name = ?";

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/clarity", "root", "");
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setString(1, newPassword);
            pst.setString(2, facultyName);

            int rows = pst.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Password updated successfully.");
                //fetchFaculty(); // refresh table
            } else {
                JOptionPane.showMessageDialog(this, "Update failed.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }



    }//GEN-LAST:event_Upt_facActionPerformed

