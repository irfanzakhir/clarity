    private void report_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_report_btnActionPerformed
        // TODO add your handling code here:
         String sessionId = Datas.selectedSessionId;
    String facultyName = Datas.loggedInFacultyName;

    // Ask faculty for a reason
    String reason = JOptionPane.showInputDialog(this, "Enter reason for reporting:");

    if (reason == null || reason.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Report cancelled. Reason is required.");
        return;
    }

    String sql = "INSERT INTO reports (session_id, reported_by, reason) VALUES (?, ?, ?)";

    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, sessionId);
        pst.setString(2, facultyName);
        pst.setString(3, reason);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Query reported to Admin successfully!");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error reporting query: " + e.getMessage());
    }


    }//GEN-LAST:event_report_btnActionPerformed

