    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        String sessionId = sess_id.getText().trim();         // from Datas or form field
    String responseText = st_ans_view.getText().trim();       // faculty's reply
    String status = "Resolved";                               // or get from dropdown if needed

    if (responseText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a response before submitting.");
        return;
    }

    String sql = "UPDATE queries SET faculty_response = ?, responded_at = NOW(), status = ? WHERE session_id = ?";

    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, responseText);
        pst.setString(2, status);
        pst.setString(3, sessionId);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Response submitted successfully!");
            st_ans_view.setText(""); // clear response box
        } else {
            JOptionPane.showMessageDialog(this, "Failed to submit response. Check session ID.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }

    }//GEN-LAST:event_submit_btnActionPerformed

