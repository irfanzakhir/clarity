    private void Fac_addDetails1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Fac_addDetails1ActionPerformed
        // TODO add your handling code here:
        
    int row = StDetails_table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete.");
        return;
    }

    String studentId = StDetails_table.getValueAt(row, 0).toString(); // assuming column 0 is username

    int confirm = JOptionPane.showConfirmDialog(this,
        "Are you sure you want to delete this student?",
        "Confirm Delete", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        String sql = "UPDATE student_details SET status = 'deleted' WHERE username = ?";

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/clarity", "root", "");
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setString(1, studentId);
            int rows = pst.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Student marked as deleted.");
                // Optionally refresh table
                //fetchStudents(); // your method to reload active students
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting student: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_Fac_addDetails1ActionPerformed

