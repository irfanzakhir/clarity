    private void St_details_adActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_St_details_adActionPerformed
    // TODO add your handling code here:
    try {
    
    Class.forName("com.mysql.cj.jdbc.Driver");

   
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clarity", "root", "");

    
    String query = "SELECT * FROM student_details WHERE status = 'active'";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(query);

    // Get table model
        DefaultTableModel model = (DefaultTableModel) StDetails_table.getModel();
    model.setRowCount(0); // Clear existing rows

    // Populate table
    while (rs.next()) {
        int uname = rs.getInt("username");
        int pword = rs.getInt("password");
        String year = rs.getString("year");
        


        model.addRow(new Object[]{uname, pword, year});
    }

    conn.close();
} catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading data: " + e.getMessage());
}
    }//GEN-LAST:event_St_details_adActionPerformed

