    private void fetchYr2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fetchYr2ActionPerformed
        // TODO add your handling code here:
        String facultyName = Datas.loggedInFacultyName;
        String year = "1st Year"; // hardcoded for Yr_1 page

        String sql = "SELECT session_id, query_text, follow_up_text, status, submitted_at " +
        "FROM queries WHERE faculty_name = ? AND year = ? ORDER BY submitted_at DESC";

        try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
        PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, facultyName);
        pst.setString(2, year);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblQueries.getModel();
        model.setRowCount(0); // clear old data

        while (rs.next()) {
            String sessionId = rs.getString("session_id");
            String query = rs.getString("query_text");
            String followUp = rs.getString("follow_up_text"); // ✅ new
            String status = rs.getString("status");
            Timestamp date = rs.getTimestamp("submitted_at");

            model.addRow(new Object[]{sessionId, query, followUp, status, date});
        }
        for (int row = 0; row < tblQueries.getRowCount(); row++) {
            int rowHeight = tblQueries.getRowHeight();

            Component comp = tblQueries.prepareRenderer(tblQueries.getCellRenderer(row, 1), row, 1);
            int preferredHeight = comp.getPreferredSize().height;

            if (preferredHeight > rowHeight) {
                tblQueries.setRowHeight(row, preferredHeight);
            }
        }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error fetching data: " + e.getMessage());
        }
    }//GEN-LAST:event_fetchYr2ActionPerformed

