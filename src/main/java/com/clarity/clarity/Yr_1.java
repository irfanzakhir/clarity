    private void resolve_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resolve_btnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblQueries.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a query to resolve.");
            return;
        }

        String sessionId = tblQueries.getValueAt(selectedRow, 0).toString();
        String queryText = tblQueries.getValueAt(selectedRow, 1).toString();

        // ✅ Safely handle null follow-up
        Object followUpObj = tblQueries.getValueAt(selectedRow, 2);
        String followUp = (followUpObj != null) ? followUpObj.toString() : "";

        // ✅ Prefer follow-up if available
        String displayText = (!followUp.isEmpty()) ? followUp : queryText;

        Datas.selectedSessionId = sessionId;
        Datas.selectedQueryText = displayText;

        new FacultyRespond().setVisible(true);
        this.dispose();

    }//GEN-LAST:event_resolve_btnActionPerformed

