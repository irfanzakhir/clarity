    private void fetchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fetchActionPerformed
        // TODO add your handling code here:

    // ✅ Use the student ID stored at login
    String studentId = Datas.loggedInStudentId;

    if (studentId == null || studentId.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Student ID not found. Please log in again.");
        return;
    }

    String sql = "SELECT session_id, query_text, faculty_response, follow_up_text, status " +
                 "FROM queries WHERE student_id = ? ORDER BY submitted_at DESC";

    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/clarity", "root", "");
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, studentId);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblMyQueries.getModel();
        model.setRowCount(0); // clear old data

        while (rs.next()) {
            String sessionId = rs.getString("session_id");
            String query = rs.getString("query_text");
            String facultyResp = rs.getString("faculty_response");
            String followUp = rs.getString("follow_up_text");
            String status = rs.getString("status");

            model.addRow(new Object[]{sessionId, query, facultyResp, followUp, status});
        }

        // ===== COLOR-CODE STATUS COLUMN =====
        tblMyQueries.getColumnModel().getColumn(4).setCellRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String status = value != null ? value.toString() : "";
                if (!isSelected) {
                    if (status.equalsIgnoreCase("Resolved")) {
                        c.setBackground(new Color(198, 239, 206)); // light green
                    } else if (status.equalsIgnoreCase("Follow-up")) {
                        c.setBackground(new Color(255, 235, 156)); // light yellow
                    } else {
                        c.setBackground(Color.WHITE);
                    }
                }
                return c;
            }
        });

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error fetching queries: " + e.getMessage());
    }

    }//GEN-LAST:event_fetchActionPerformed

