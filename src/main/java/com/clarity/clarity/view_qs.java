    private void follow_up_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_follow_up_btnActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblMyQueries.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a query first.");
        return;
    }

    String sessionId = tblMyQueries.getValueAt(selectedRow, 0).toString(); // Session ID
    String followUpMsg = JOptionPane.showInputDialog(this, "Enter your follow-up:");

    if (followUpMsg != null && !followUpMsg.trim().isEmpty()) {
        String sql = "UPDATE queries SET follow_up_text = ?, status = 'Follow-up', last_updated = NOW() WHERE session_id = ?";

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/clarity", "root", "");
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setString(1, followUpMsg);
            pst.setString(2, sessionId);

            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Follow-up submitted!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }


    }//GEN-LAST:event_follow_up_btnActionPerformed

